							公益財団法人アジア学生文化協会は1957年（昭和32年）に設立されました。本協会のルーツは本郷の東京大学正門前にある新星学寮で、その歴史は大正時代、上杉慎吉東大教授が主宰した「至軒学堂」まで遡ります。戦前は青年学生による社会改造が志向されましたが、戦後は「新星学寮」と名称を変え穂積五一師の指導の下、アジアに対する日本の戦争責任を深く内省し、自主、互恵、平等の精神で寮生自身による自治運営がなされ現在に至っています。本協会はこのような生活の場を修養・教育の場として、人間形成の基礎に置くという歴史を背景としつつ、世界各国とりわけアジア諸国との真の友好交流と、それを支える人材育成を目的として各種事業を行って参りました。<br><br>
							現代の社会は経済、技術の発達によりあらゆる分野で国境を超えた繋がりが極めて緊密になり、人の移動も盛んになりました。一方ではそれ故の複雑化した新たな問題、特に環境問題や格差問題が生じ、残念ながら国際間の対立や紛争も絶えることなく起こっています。60年以上前に書かれた協会の「設立趣意書」には国、地域、民族、宗教を超え「互恵協力」により「人間的和合」を目指す目標が高らかに表明されており、その内容は今読み返しても古色おびることなく、さらに重要性を増していると言えます。<br><br>
							本協会は、主として留学生の勉学、受入れを支援し、日本人学生、留学生の交流・生活を支え、大学等の国際教育活動を促進するという活動を行っておりますが、さらに増幅しつつある社会の将来課題に対しても果敢に挑戦し、真の国際相互理解と世界平和の構築に貢献したいと願っています。これら本協会の事業は、内外の広範なご理解、ご協力を得て実現されるものであり、引き続き皆様の熱意あるご支援ご鞭撻をお願いする次第です。<br><br>
