			<h2 class="h_type_1">3.	当協会への会費・<br class="pc">寄付金に対する免税措置について</h2>
			<span>Tax exemption</span>
			<span class="in_tx">
当協会は、内閣総理大臣より「公益財団法人」の認定を受けており、 当協会への寄付金には「特定公益増進法人」としての税制上の優遇措置が適用され、次の控除が認められております。<br>
<br>
【所轄税務署への確定申告の際は、当協会が発行する領収証とあわせて「税額控除に係る証明書」（写）の添付が必要となります。年末調整等では控除されませんのでご注意下さい。】
			</span>
			<h3 class="h_type_5 mt_40">①個人の場合 所得税</h3>
			<span class="in_tx">
「所得控除」又は「税額控除」のうち、ご本人に有利な方式で控除が受けられます。<br>
<br>
○所得控除<br> 
[ 総所得金額－（所得控除対象寄付金(※1)－2,000円）]×税率＝所得税額 <br>
（※1）所得控除対象寄付金：総所得の40％が上限。<br>
他の「特定公益増進法人」への寄付金が合算できます。<br>
<br>				
○税額控除<br>
 所得税額－ [（税額控除対象寄付金(※2)-2,000円）×40％＝控除対象額（※3）] <br>
（※2）税額控除対象寄付金：総所得の40％が上限。<br>
（※3）税額控除対象額：所得税額の25％が上限。<br>
 他の「所得税の税額控除適用法人」への寄付金が合算できます。 <br>
<br>
○相続税<br>
遺贈又は相続により取得した財産を、相続税の申告期限内に当会にご寄付いただくと、その財産には相続税が課税されません。
			</span>
			<h3 class="h_type_5 mt_40">②法人の場合</h3>
			<span class="in_tx">
公益財団法人に対する寄附金は、一定の損金算入限度額に相当する金額まで、一般の寄附金とは別枠で損金として扱うことができます。<br>
また法人地方税は法人税の納付額を基礎に計算されますので、法人税の減免に伴い地方税も減額となります。
			</span>
